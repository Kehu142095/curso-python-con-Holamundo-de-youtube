"""modulos."""
import math

print("\n")
print(round(5.6))  # devolvera en la terminal 6
print(round(5.2))  # devolvera en la terminal 5
# Round: devuelve el numero en redondeo

print(abs(-56))  # devolvera 56
print(abs(+56))  # devolvera 56
# abs: devuelve el numero absoluto del que le pongamos, le quita el menos o el mas

print(math.ceil(5.6))  # va a devolver el numero superior entero mas cercano
print(math.floor(5.1))  # va a devolver el numero inferior entero mas cercano
print(math.isnan(23))
# isnan: va a devolver si es que el valor que le ponemos entre parentesis "No es un numero" lo devuelve en boolean:False o True
# print(math.isnan("23"))
print(math.pow(20, 6))
# va a delvolver el numero elevado al numero que pongamos despues de la coma
print(math.sqrt(56))  # devolvera la raiz cuadrada del numero que le pongamos
